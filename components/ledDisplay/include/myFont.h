static void getFontBuf(fontTypeEnum_t fontType, char chr, fontInfoStr_t *info)
{
    if(fontType == SONT_TI)
    {
        info->pBuf = (unsigned char *)(&songTiCharBuf[chr - ' '][0]);
        info->height = SONG_TI_CHAR_FONT_HEIGHT;
        info->width = SONG_TI_CHAR_FONT_WIDTH;
    }
    else if(fontType == FIXEDSYS)
    {
        info->pBuf = (unsigned char *)(&fixedsysCharBuf[chr - ' '][0]);
        info->height = FIXEDSYS_CHAR_FONT_HEIGHT;
        info->width = FIXEDSYS_CHAR_FONT_WIDTH;
    }
}

static void getFontInfo(fontTypeEnum_t fontType, fontInfoStr_t *info)
{
    getFontBuf(fontType, ' ', info);
}

